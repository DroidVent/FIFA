package com.firstbit.fifaworldcup2018highlights.data;

public class Standing {
    private String MP;
    private String W;
    private String D;
    private String L;
    private String GF;
    private String GA;
    private String GD;
    private String Pts;

    public String getMP() {
        return MP;
    }

    public void setMP(String MP) {
        this.MP = MP;
    }

    public String getW() {
        return W;
    }

    public void setW(String w) {
        W = w;
    }

    public String getD() {
        return D;
    }

    public void setD(String d) {
        D = d;
    }

    public String getL() {
        return L;
    }

    public void setL(String l) {
        L = l;
    }

    public String getGF() {
        return GF;
    }

    public void setGF(String GF) {
        this.GF = GF;
    }

    public String getGA() {
        return GA;
    }

    public void setGA(String GA) {
        this.GA = GA;
    }

    public String getGD() {
        return GD;
    }

    public void setGD(String GD) {
        this.GD = GD;
    }

    public String getPts() {
        return Pts;
    }

    public void setPts(String pts) {
        Pts = pts;
    }
}
